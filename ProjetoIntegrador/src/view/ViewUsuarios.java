    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        // TODO add your handling code here:
        if (salvarAlterar.equals("salvar")) {
            modelUsuarios.setIdUsuario(Integer.parseInt(this.codigoUsuario.getText()));
            modelUsuarios.setNomeUsuario(this.nomeUsuario.getText());
            modelUsuarios.setLoginUsuario(this.textoLogin.getText());
            modelUsuarios.setSenhaUsuario(this.textoSenha.getText());
        
            if (controllerUsuario.salvarUsuariosController(modelUsuarios) > 0) {
                JOptionPane.showMessageDialog(this, "Registro salvo com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                //carregar os clientes na tabela
                carregarUsuarios();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar registro!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            modelUsuarios.setIdUsuario(Integer.parseInt(this.codigoUsuario.getText()));
            modelUsuarios.setNomeUsuario(this.nomeUsuario.getText());
            modelUsuarios.setLoginUsuario(this.textoLogin.getText());
            modelUsuarios.setSenhaUsuario(this.textoSenha.getText());

            if(controllerUsuario.alterarUsuariosController(modelUsuarios)) {
                JOptionPane.showMessageDialog(this, "Alteração salva com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
                //carregar os clientes na tabela
                carregarUsuarios();
                this.habilitarDesabilitarCampos(false);
                this.limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao alterar registro!", "ERRO", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        // TODO add your handling code here:
        int linha = tabelaUsuario.getSelectedRow();
        int codigoCliente = (int) tabelaUsuario.getValueAt(linha, 0);

        if (controllerUsuario.excluirUsuariosController(codigoCliente)) {
            JOptionPane.showMessageDialog(this, "Usuario excluido com sucesso!", "ATENÇÃO", JOptionPane.WARNING_MESSAGE);
            this.carregarUsuarios();
            this.habilitarDesabilitarCampos(false);
        } else {
            JOptionPane.showConfirmDialog(this, "Erro ao usuario o cliente", "ERRO", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoExcluirActionPerformed

    private void botaoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarActionPerformed
        // TODO add your handling code here:
        int linha = tabelaUsuario.getSelectedRow();
        int codigoUsuario2 = (int) tabelaUsuario.getValueAt(linha, 0);
        salvarAlterar = "alterar";

        modelUsuarios = controllerUsuario.retornaUsuariosController(codigoUsuario2);
        codigoUsuario.setText(String.valueOf(modelUsuarios.getIdUsuario()));
        nomeUsuario.setText(modelUsuarios.getNomeUsuario());
        textoLogin.setText(modelUsuarios.getNomeUsuario());
        textoSenha.setText(modelUsuarios.getNomeUsuario());
        
        
        
        this.habilitarDesabilitarCampos(true);
    }//GEN-LAST:event_botaoAlterarActionPerformed

