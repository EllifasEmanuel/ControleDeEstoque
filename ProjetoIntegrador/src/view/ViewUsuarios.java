    //carregar usuarios da tabela
    private void carregarUsuarios() {
        listaModelUsuarios = controllerUsuario.retornaListaUsuariosController();
        DefaultTableModel modelo = (DefaultTableModel) tabelaUsuario.getModel();
        modelo.setNumRows(0);

        int cont = listaModelUsuarios.size();
        for (int i = 0; i < cont; i++) {
            modelo.addColumn(new Object[]{
                listaModelUsuarios.get(i).getIdUsuario(),
                listaModelUsuarios.get(i).getNomeUsuario(),
                listaModelUsuarios.get(i).getLoginUsuario(),
                listaModelUsuarios.get(i).getSenhaUsuario(),});
        }
    }

    private void limparCampos() {
        codigoUsuario.setText("");
        nomeUsuario.setText("");
        textoLogin.setText("");
        textoSenha.setText("");
    }

    private void habilitarDesabilitarCampos(boolean condicao) {
        codigoUsuario.setEnabled(condicao);
        nomeUsuario.setEnabled(condicao);
        textoLogin.setEnabled(condicao);
        textoSenha.setEnabled(condicao);
        botaoSalvar.setEnabled(condicao);
    }

