    private void salvarProduto(){
        modelProdutos.setIdProduto(Integer.parseInt(this.codigoProduto.getText()));
        modelProdutos.setNomeProduto(this.nomeProduto.getText());
        modelProdutos.setPrecoProduto(formatador.converterVirgulaParaPontoReturn(this.valorProduto.getText()));
        modelProdutos.setEstoqueProduto(Integer.parseInt(this.quantidadeProduto.getText()));
        
        if(controllerProdutos.salvarProdutoController(modelProdutos)>0){
            JOptionPane.showMessageDialog(this, "Produto cadastrado com sucesso!", "ATENÇÃO",JOptionPane.WARNING_MESSAGE );
            this.preencherProdutos();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar o produto!,", "ERRO",JOptionPane.ERROR_MESSAGE );
        }
    }
    
    private void alterarProduto(){
        //Alterar um produto
        modelProdutos.setIdProduto(Integer.parseInt(this.codigoProduto.getText()));
        modelProdutos.setNomeProduto(this.nomeProduto.getText());
        modelProdutos.setPrecoProduto(formatador.converterVirgulaParaPontoReturn(this.valorProduto.getText()));
        modelProdutos.setEstoqueProduto(Integer.parseInt(this.quantidadeProduto.getText()));
        
        if(controllerProdutos.alterarProdutoController(modelProdutos)){
            JOptionPane.showMessageDialog(this, "Produto alterado com sucesso!", "ATENÇÃO",JOptionPane.WARNING_MESSAGE );
            this.preencherProdutos();
            this.limparCampos();
            this.habilitarDesabilitarCampos(false);
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao alterar o produto!,", "ERRO",JOptionPane.ERROR_MESSAGE );
        }
    }
    
    private void habilitarDesabilitarCampos(boolean condicao){
        codigoProduto.setEnabled(condicao);
        nomeProduto.setEnabled(condicao);
        valorProduto.setEnabled(condicao);
        quantidadeProduto.setEnabled(condicao);
        botaoSalvar.setEnabled(condicao);
    }
    
    private void limparCampos(){
        codigoProduto.setText("");
        nomeProduto.setText("");
        valorProduto.setText("");
        quantidadeProduto.setText("");
    }
    
    //Preencher a lista de produtos do banco de dados
    private void preencherProdutos(){
       listaModelProdutos = controllerProdutos.retornaListaProdutoController();
       DefaultTableModel modelo = (DefaultTableModel) tabelaProdutos.getModel();
       modelo.setNumRows(0);
       //Inserir os produtos na tabela
       int cont = listaModelProdutos.size();
       for(int i=0; i<cont ;i++){
           modelo.addRow(new Object[]{
               listaModelProdutos.get(i).getIdProduto(),
               listaModelProdutos.get(i).getNomeProduto(),
               listaModelProdutos.get(i).getPrecoProduto(),
               listaModelProdutos.get(i).getEstoqueProduto()
           });
       }
    }
