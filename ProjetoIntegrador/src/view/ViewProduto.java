    private void botaoAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAlterarActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "alterar";
        habilitarDesabilitarCampos(true);
        int linha = this.tabelaProdutos.getSelectedRow();
        
        try{
            int codigoProduto = (int) this.tabelaProdutos.getValueAt(linha, 0);
            //recuperar dados do banco
            modelProdutos = controllerProdutos.retornaProdutoController(codigoProduto);
            //setar na interface
            this.codigoProduto.setText(String.valueOf(modelProdutos.getIdProduto()));
            this.nomeProduto.setText(modelProdutos.getNomeProduto());
            this.valorProduto.setText(String.valueOf(modelProdutos.getPrecoProduto()));
            this.quantidadeProduto.setText(String.valueOf(modelProdutos.getEstoqueProduto()));
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(this, "Código inválido ou nenhum registro selecionado","Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_botaoAlterarActionPerformed

    private void botaoPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoPesquisarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) this.tabelaProdutos.getModel();
        final TableRowSorter<TableModel> classificador = new TableRowSorter<>(modelo);
        this.tabelaProdutos.setRowSorter(classificador);
        String texto = pesquisarProduto.getText();
        classificador.setRowFilter(RowFilter.regexFilter(texto, 1));
    }//GEN-LAST:event_botaoPesquisarActionPerformed

